def save_report(report: AnalysisReport) -> Path:
    """Auto-save a codebase analysis to ~/.stride-gpt/reports/analyze/.

    Returns the saved path.
    """
    from stride_gpt.config import analyze_reports_dir

    target_dir = analyze_reports_dir()
    target_dir.mkdir(parents=True, exist_ok=True)

    target_name = Path(report.plan.target_path).name
    name = re.sub(r"[^a-zA-Z0-9_-]", "-", target_name) or "analysis"
    timestamp = datetime.now(timezone.utc).strftime("%Y-%m-%dT%H%M%SZ")
    filename = f"{name}_{timestamp}.json"

    path = target_dir / filename
    rendered = render_json(report)
    path.write_text(json.dumps(rendered, indent=2) + "\n")
    _write_html_companion(path, rendered)
    return path


def save_quick_report(
    output: ThreatModelOutput,
    name: str,
    *,
    app_type_hint: str | None = None,
    models: ModelPair | None = None,
) -> Path:
    """Auto-save a /quick threat model to ~/.stride-gpt/reports/quick/.

    The serialised shape mirrors the agentic report just enough for
    ``list_reports`` and ``render_markdown_from_json`` to work without
    special-casing — `kind: "quick"`, a single synthetic subsystem named
    "Application", and a metadata block. Returns the saved path.
    """
    from stride_gpt.config import quick_reports_dir

    target_dir = quick_reports_dir()
    target_dir.mkdir(parents=True, exist_ok=True)

    safe_name = re.sub(r"[^a-zA-Z0-9_-]", "-", name) or "quick-analysis"
    timestamp = datetime.now(timezone.utc).strftime("%Y-%m-%dT%H%M%SZ")
    filename = f"{safe_name}_{timestamp}.json"

    metadata: dict[str, Any] = {"kind": "quick"}
    if models is not None:
        metadata["worker_model"] = models.worker.model_name
        metadata["worker_provider"] = models.worker.provider
        metadata["architect_model"] = (
            models.architect.model_name if models.tiered else None
        )
        metadata["architect_provider"] = (
            models.architect.provider if models.tiered else None
        )
    if app_type_hint:
        metadata["app_type_hint"] = app_type_hint
    metadata["llm_calls"] = output.llm_calls
    metadata["tool_calls"] = output.tool_calls
    metadata["tools_used"] = dict(output.tools_used)

    data = {
        "version": "1.0",
        "kind": "quick",
        "generated_at": datetime.now(timezone.utc).isoformat(),
        "target": name,
        "overview": "",
        "subsystems": [
            {
                "name": "Application",
                "threats": output.threat_model,
                "improvement_suggestions": output.improvement_suggestions,
                "files_analyzed": [],
            }
        ],
        "cross_cutting_threats": [],
        "metadata": metadata,
    }

    path = target_dir / filename
    path.write_text(json.dumps(data, indent=2) + "\n")
    _write_html_companion(path, data)
    return path


def _write_html_companion(json_path: Path, data: dict[str, Any]) -> None:
    """Auto-save an HTML view next to a JSON report.

    Best-effort: a render bug must not lose the JSON write the user just
    finished waiting for. We catch broadly and surface to stderr so failures
    are visible without crashing the save path.
    """
    try:
        from stride_gpt.agent.html_report import render_html_from_json
        html_path = json_path.with_suffix(".html")
        html_path.write_text(render_html_from_json(data))
    except Exception as exc:  # noqa: BLE001 — see docstring
        import sys
        print(
            f"[stride-gpt] warning: HTML companion failed: {exc}",
            file=sys.stderr,
        )


def load_report(path: Path) -> dict[str, Any]:
    """Load a saved report JSON file. Returns the parsed dict."""
    return json.loads(path.read_text())


def list_reports(
    limit: int = 10,
    *,
    kind: str = "analyze",
) -> list[tuple[int, Path, dict[str, Any]]]:
    """List recent saved reports, newest first.

    Args:
        limit: Maximum number of reports to return.
        kind: ``"analyze"`` (default) for codebase analyses,
            ``"quick"`` for description-based analyses, or ``"all"`` to merge
            both directories. Legacy reports living at the root of
            ``~/.stride-gpt/reports/`` (pre-split layout) are always included
            for backwards compatibility and tagged with ``kind: "legacy"`` in
            the summary.

    Returns list of ``(index, path, summary_dict)`` tuples.
    ``summary_dict`` has: ``generated_at``, ``target``, ``threat_count``,
    ``model``, ``subsystem_count``, ``kind``.
    """
    from stride_gpt.config import (
        REPORTS_DIR,
        analyze_reports_dir,
        quick_reports_dir,
    )

    dirs: list[tuple[Path, str]] = []
    if kind in ("analyze", "all"):
        dirs.append((analyze_reports_dir(), "analyze"))
    if kind in ("quick", "all"):
        dirs.append((quick_reports_dir(), "quick"))

    # Pre-split reports live in the root reports/ dir. Surface them whenever
    # /reports is called so users can find their old work, tagged as legacy.
    legacy_files: list[Path] = []
    if REPORTS_DIR.is_dir():
        legacy_files = [
            p for p in REPORTS_DIR.glob("*.json") if p.is_file()
        ]

    candidates: list[tuple[Path, str]] = []
    for d, d_kind in dirs:
        if d.is_dir():
            candidates.extend((p, d_kind) for p in d.glob("*.json"))
    candidates.extend((p, "legacy") for p in legacy_files)

    candidates.sort(key=lambda pair: pair[0].stat().st_mtime, reverse=True)

    results: list[tuple[int, Path, dict[str, Any]]] = []
    for i, (path, path_kind) in enumerate(candidates[:limit], 1):
        try:
            data = json.loads(path.read_text())
            threat_count = sum(len(s.get("threats", [])) for s in data.get("subsystems", []))
            threat_count += len(data.get("cross_cutting_threats", []))
            summary = {
                "generated_at": data.get("generated_at", ""),
                "target": data.get("target", ""),
                "threat_count": threat_count,
                "model": data.get("metadata", {}).get("worker_model", ""),
                "subsystem_count": len(data.get("subsystems", [])),
                "kind": path_kind,
            }
            results.append((i, path, summary))
        except (json.JSONDecodeError, OSError):
            continue

    return results


def render_markdown_from_json(data: dict[str, Any]) -> str:
    """Re-render a saved JSON report as markdown."""
    project_name = Path(data.get("target", "unknown")).name
    lines: list[str] = []

    lines.append(f"# STRIDE Threat Model: {project_name}")
    lines.append("")
    generated = data.get("generated_at", "")
    if generated:
        lines.append(f"> Generated by STRIDE-GPT on {generated}")
    lines.append("")

    lines.append("## Overview")
    lines.append("")
    lines.append(data.get("overview", ""))
    lines.append("")

    cross_cutting = data.get("cross_cutting_threats", [])
    all_threats: list[dict[str, Any]] = []
    for sub in data.get("subsystems", []):
        all_threats.extend(sub.get("threats", []))
    all_threats.extend(cross_cutting)
    show_llm, show_asi, show_insider = detect_extra_columns(all_threats)

    for sub in data.get("subsystems", []):
        lines.append(f"## {sub['name']}")
        lines.append("")

        files = sub.get("files_analyzed", [])
        if files:
            lines.append("### Files Analyzed")
            lines.append("")
            for f in files:
                lines.append(f"- `{f}`")
            lines.append("")

        threats = sub.get("threats", [])
        if threats:
            lines.append("### Threats")
            lines.append("")
            header, separator = threat_table_header(show_llm, show_asi, show_insider)
            lines.append(header)
            lines.append(separator)
            for threat in threats:
                lines.append(threat_table_row(threat, show_llm, show_asi, show_insider))
            lines.append("")

        suggestions = sub.get("improvement_suggestions", [])
        if suggestions:
            lines.append("### Recommendations")
            lines.append("")
            for s in suggestions:
                lines.append(f"- {s}")
            lines.append("")

    if cross_cutting:
        lines.append("## Cross-Cutting Threats")
        lines.append("")
        header, separator = threat_table_header(
            show_llm, show_asi, show_insider, cross_cutting=True
        )
        lines.append(header)
        lines.append(separator)
        for threat in cross_cutting:
            lines.append(
                threat_table_row(
                    threat, show_llm, show_asi, show_insider, cross_cutting=True
                )
            )
        lines.append("")

    total = sum(len(s.get("threats", [])) for s in data.get("subsystems", []))
    total += len(cross_cutting)
    metadata = data.get("metadata", {})
    lines.append("## Summary")
    lines.append("")
    lines.append(f"- **Total threats identified**: {total}")
    lines.append(f"- **Subsystems analyzed**: {len(data.get('subsystems', []))}")
    lines.append(f"- **Cross-cutting threats**: {len(cross_cutting)}")
    if metadata:
        lines.append(f"- **LLM calls**: {metadata.get('llm_calls', 'N/A')}")
        lines.append(f"- **Tool calls**: {metadata.get('tool_calls', 'N/A')}")
        worker = metadata.get("worker_model")
        worker_provider = metadata.get("worker_provider", "")
        architect = metadata.get("architect_model")
        architect_provider = metadata.get("architect_provider", "")
        if architect:
            lines.append(f"- **Architect model**: {architect_provider}/{architect}")
            lines.append(f"- **Worker model**: {worker_provider}/{worker}")
        elif worker:
            lines.append(f"- **Model**: {worker_provider}/{worker}")
    lines.append("")

    return "\n".join(lines)


