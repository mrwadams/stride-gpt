"""Report rendering — markdown, JSON, and SARIF output from analysis reports."""

from __future__ import annotations

import json
import re
from collections.abc import Iterable
from datetime import datetime, timezone
from pathlib import Path
from typing import Any

from stride_gpt.core.schemas import AnalysisReport, SubsystemFinding


def _detect_extra_columns(
    all_threats: Iterable[dict[str, Any]],
) -> tuple[bool, bool, bool]:
    """Decide which optional columns (OWASP LLM / OWASP ASI / Insider) to
    surface in the rendered tables.

    A column is shown only if at least one threat in the whole report carries
    a non-empty value for it. Computed once at the report level so every
    per-subsystem table has the same shape — partial columns per subsystem
    would look broken.
    """
    threats = list(all_threats)
    show_llm = any(t.get("OWASP_LLM") for t in threats)
    show_asi = any(t.get("OWASP_ASI") for t in threats)
    show_insider = any(t.get("INSIDER_CATEGORY") for t in threats)
    return show_llm, show_asi, show_insider


def _threat_table_header(
    show_llm: bool, show_asi: bool, show_insider: bool, *, cross_cutting: bool = False
) -> tuple[str, str]:
    cols = ["Threat Type", "Scenario", "Potential Impact"]
    if show_llm:
        cols.append("OWASP LLM")
    if show_asi:
        cols.append("OWASP ASI")
    if show_insider:
        cols.append("Insider Category")
    if cross_cutting:
        cols.append("Affected Subsystems")
    header = "| " + " | ".join(cols) + " |"
    separator = "|" + "|".join("-" * (len(c) + 2) for c in cols) + "|"
    return header, separator


def _threat_table_row(
    threat: dict[str, Any],
    show_llm: bool,
    show_asi: bool,
    show_insider: bool,
    *,
    cross_cutting: bool = False,
) -> str:
    cells = [
        threat.get("Threat Type", "Unknown"),
        str(threat.get("Scenario", "")).replace("|", "\\|"),
        str(threat.get("Potential Impact", "")).replace("|", "\\|"),
    ]
    if show_llm:
        cells.append(threat.get("OWASP_LLM") or "")
    if show_asi:
        cells.append(threat.get("OWASP_ASI") or "")
    if show_insider:
        cells.append(threat.get("INSIDER_CATEGORY") or "")
    if cross_cutting:
        cells.append(", ".join(threat.get("Affected Subsystems", [])))
    return "| " + " | ".join(cells) + " |"


def render_markdown(report: AnalysisReport) -> str:
    """Render an AnalysisReport as a full markdown document."""
    project_name = Path(report.plan.target_path).name
    lines: list[str] = []

    lines.append(f"# STRIDE Threat Model: {project_name}")
    lines.append("")
    lines.append(f"> Generated by STRIDE-GPT on {datetime.now(timezone.utc).strftime('%Y-%m-%d %H:%M UTC')}")
    lines.append("")

    # Overview
    lines.append("## Overview")
    lines.append("")
    lines.append(report.plan.overall_description)
    lines.append("")

    # Analysis plan summary
    lines.append("## Analysis Plan")
    lines.append("")
    lines.append(f"Analyzed **{len(report.findings)}** subsystems:")
    lines.append("")
    for sub in report.plan.subsystems:
        lines.append(f"- **{sub.name}**: {sub.description}")
    lines.append("")

    show_llm, show_asi, show_insider = _detect_extra_columns(
        [t for f in report.findings for t in f.threats] + list(report.cross_cutting_threats)
    )

    # Per-subsystem findings
    for finding in report.findings:
        lines.append(f"## {finding.subsystem}")
        lines.append("")

        if finding.files_analyzed:
            lines.append("### Files Analyzed")
            lines.append("")
            for f in finding.files_analyzed:
                lines.append(f"- `{f}`")
            lines.append("")

        if finding.threats:
            lines.append("### Threats")
            lines.append("")
            header, separator = _threat_table_header(show_llm, show_asi, show_insider)
            lines.append(header)
            lines.append(separator)
            for threat in finding.threats:
                lines.append(_threat_table_row(threat, show_llm, show_asi, show_insider))
            lines.append("")

        if finding.improvement_suggestions:
            lines.append("### Recommendations")
            lines.append("")
            for suggestion in finding.improvement_suggestions:
                lines.append(f"- {suggestion}")
            lines.append("")

    # Cross-cutting threats
    if report.cross_cutting_threats:
        lines.append("## Cross-Cutting Threats")
        lines.append("")
        header, separator = _threat_table_header(
            show_llm, show_asi, show_insider, cross_cutting=True
        )
        lines.append(header)
        lines.append(separator)
        for threat in report.cross_cutting_threats:
            lines.append(
                _threat_table_row(
                    threat, show_llm, show_asi, show_insider, cross_cutting=True
                )
            )
        lines.append("")

    # Summary
    total_threats = sum(len(f.threats) for f in report.findings) + len(report.cross_cutting_threats)
    lines.append("## Summary")
    lines.append("")
    lines.append(f"- **Total threats identified**: {total_threats}")
    lines.append(f"- **Subsystems analyzed**: {len(report.findings)}")
    lines.append(f"- **Cross-cutting threats**: {len(report.cross_cutting_threats)}")
    if report.metadata:
        lines.append(f"- **LLM calls**: {report.metadata.get('llm_calls', 'N/A')}")
        lines.append(f"- **Tool calls**: {report.metadata.get('tool_calls', 'N/A')}")
    lines.append("")

    return "\n".join(lines)


def render_json(report: AnalysisReport) -> dict[str, Any]:
    """Render an AnalysisReport as a structured JSON-serializable dict."""
    return {
        "version": "1.0",
        "generated_at": datetime.now(timezone.utc).isoformat(),
        "target": report.plan.target_path,
        "overview": report.plan.overall_description,
        "subsystems": [
            {
                "name": f.subsystem,
                "threats": f.threats,
                "improvement_suggestions": f.improvement_suggestions,
                "files_analyzed": f.files_analyzed,
            }
            for f in report.findings
        ],
        "cross_cutting_threats": report.cross_cutting_threats,
        "metadata": report.metadata,
    }


def render_sarif(report: AnalysisReport) -> dict[str, Any]:
    """Render an AnalysisReport as SARIF 2.1.0 format.

    SARIF (Static Analysis Results Interchange Format) is supported by
    GitHub, GitLab, Azure DevOps, and many IDEs for security findings.
    """
    rules: list[dict[str, Any]] = []
    results: list[dict[str, Any]] = []
    rule_ids: set[str] = set()

    def _make_rule_id(threat_type: str) -> str:
        return "STRIDE/" + threat_type.upper().replace(" ", "_")

    # Process per-subsystem threats
    for finding in report.findings:
        for threat in finding.threats:
            threat_type = threat.get("Threat Type", "Unknown")
            rule_id = _make_rule_id(threat_type)

            if rule_id not in rule_ids:
                rule_ids.add(rule_id)
                rules.append({
                    "id": rule_id,
                    "name": threat_type,
                    "shortDescription": {"text": f"STRIDE: {threat_type}"},
                    "helpUri": "https://learn.microsoft.com/en-us/azure/security/develop/threat-modeling-tool-threats",
                })

            result_entry: dict[str, Any] = {
                "ruleId": rule_id,
                "level": "warning",
                "message": {
                    "text": f"{threat.get('Scenario', '')}\n\nPotential Impact: {threat.get('Potential Impact', '')}",
                },
                "properties": {
                    "subsystem": finding.subsystem,
                },
            }
            if threat.get("OWASP_LLM"):
                result_entry["properties"]["owasp_llm"] = threat["OWASP_LLM"]
            if threat.get("OWASP_ASI"):
                result_entry["properties"]["owasp_asi"] = threat["OWASP_ASI"]
            if threat.get("INSIDER_CATEGORY"):
                result_entry["properties"]["insider_category"] = threat["INSIDER_CATEGORY"]

            # Add location if we know which files were analyzed
            if finding.files_analyzed:
                result_entry["locations"] = [
                    {
                        "physicalLocation": {
                            "artifactLocation": {"uri": f},
                        }
                    }
                    for f in finding.files_analyzed[:3]  # Limit to avoid bloat
                ]

            results.append(result_entry)

    # Process cross-cutting threats
    for threat in report.cross_cutting_threats:
        threat_type = threat.get("Threat Type", "Unknown")
        rule_id = _make_rule_id(threat_type)

        if rule_id not in rule_ids:
            rule_ids.add(rule_id)
            rules.append({
                "id": rule_id,
                "name": threat_type,
                "shortDescription": {"text": f"STRIDE: {threat_type}"},
            })

        cross_entry: dict[str, Any] = {
            "ruleId": rule_id,
            "level": "warning",
            "message": {
                "text": f"[Cross-cutting] {threat.get('Scenario', '')}\n\nPotential Impact: {threat.get('Potential Impact', '')}",
            },
            "properties": {
                "subsystem": "cross-cutting",
                "affected_subsystems": threat.get("Affected Subsystems", []),
            },
        }
        if threat.get("OWASP_LLM"):
            cross_entry["properties"]["owasp_llm"] = threat["OWASP_LLM"]
        if threat.get("OWASP_ASI"):
            cross_entry["properties"]["owasp_asi"] = threat["OWASP_ASI"]
        if threat.get("INSIDER_CATEGORY"):
            cross_entry["properties"]["insider_category"] = threat["INSIDER_CATEGORY"]
        results.append(cross_entry)

    return {
        "$schema": "https://raw.githubusercontent.com/oasis-tcs/sarif-spec/main/sarif-2.1/schema/sarif-schema-2.1.0.json",
        "version": "2.1.0",
        "runs": [
            {
                "tool": {
                    "driver": {
                        "name": "STRIDE-GPT",
                        "informationUri": "https://github.com/mrwadams/stride-gpt",
                        "version": "0.16.0",
                        "rules": rules,
                    }
                },
                "results": results,
            }
        ],
    }


