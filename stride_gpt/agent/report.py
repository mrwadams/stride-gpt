def save_report(report: AnalysisReport) -> Path:
    """Auto-save report JSON to ~/.stride-gpt/reports/. Returns saved path."""
    from stride_gpt.config import REPORTS_DIR

    REPORTS_DIR.mkdir(parents=True, exist_ok=True)

    target_name = Path(report.plan.target_path).name
    name = re.sub(r"[^a-zA-Z0-9_-]", "-", target_name) or "analysis"
    timestamp = datetime.now(timezone.utc).strftime("%Y-%m-%dT%H%M%SZ")
    filename = f"{name}_{timestamp}.json"

    path = REPORTS_DIR / filename
    path.write_text(json.dumps(render_json(report), indent=2) + "\n")
    return path


def load_report(path: Path) -> dict[str, Any]:
    """Load a saved report JSON file. Returns the parsed dict."""
    return json.loads(path.read_text())


def list_reports(limit: int = 10) -> list[tuple[int, Path, dict[str, Any]]]:
    """List recent saved reports, newest first.

    Returns list of (index, path, summary_dict) tuples.
    summary_dict has: generated_at, target, threat_count, model.
    """
    from stride_gpt.config import REPORTS_DIR

    if not REPORTS_DIR.is_dir():
        return []

    report_files = sorted(REPORTS_DIR.glob("*.json"), key=lambda p: p.stat().st_mtime, reverse=True)

    results: list[tuple[int, Path, dict[str, Any]]] = []
    for i, path in enumerate(report_files[:limit], 1):
        try:
            data = json.loads(path.read_text())
            threat_count = sum(len(s.get("threats", [])) for s in data.get("subsystems", []))
            threat_count += len(data.get("cross_cutting_threats", []))
            summary = {
                "generated_at": data.get("generated_at", ""),
                "target": data.get("target", ""),
                "threat_count": threat_count,
                "model": data.get("metadata", {}).get("model", ""),
                "subsystem_count": len(data.get("subsystems", [])),
            }
            results.append((i, path, summary))
        except (json.JSONDecodeError, OSError):
            continue

    return results


def render_markdown_from_json(data: dict[str, Any]) -> str:
    """Re-render a saved JSON report as markdown."""
    project_name = Path(data.get("target", "unknown")).name
    lines: list[str] = []

    lines.append(f"# STRIDE Threat Model: {project_name}")
    lines.append("")
    generated = data.get("generated_at", "")
    if generated:
        lines.append(f"> Generated by STRIDE-GPT on {generated}")
    lines.append("")

    lines.append("## Overview")
    lines.append("")
    lines.append(data.get("overview", ""))
    lines.append("")

    for sub in data.get("subsystems", []):
        lines.append(f"## {sub['name']}")
        lines.append("")

        files = sub.get("files_analyzed", [])
        if files:
            lines.append("### Files Analyzed")
            lines.append("")
            for f in files:
                lines.append(f"- `{f}`")
            lines.append("")

        threats = sub.get("threats", [])
        if threats:
            lines.append("### Threats")
            lines.append("")
            lines.append("| Threat Type | Scenario | Potential Impact |")
            lines.append("|-------------|----------|------------------|")
            for threat in threats:
                t_type = threat.get("Threat Type", "Unknown")
                scenario = threat.get("Scenario", "").replace("|", "\\|")
                impact = threat.get("Potential Impact", "").replace("|", "\\|")
                lines.append(f"| {t_type} | {scenario} | {impact} |")
            lines.append("")

        suggestions = sub.get("improvement_suggestions", [])
        if suggestions:
            lines.append("### Recommendations")
            lines.append("")
            for s in suggestions:
                lines.append(f"- {s}")
            lines.append("")

    cross_cutting = data.get("cross_cutting_threats", [])
    if cross_cutting:
        lines.append("## Cross-Cutting Threats")
        lines.append("")
        lines.append("| Threat Type | Scenario | Potential Impact | Affected Subsystems |")
        lines.append("|-------------|----------|------------------|---------------------|")
        for threat in cross_cutting:
            t_type = threat.get("Threat Type", "Unknown")
            scenario = threat.get("Scenario", "").replace("|", "\\|")
            impact = threat.get("Potential Impact", "").replace("|", "\\|")
            affected = ", ".join(threat.get("Affected Subsystems", []))
            lines.append(f"| {t_type} | {scenario} | {impact} | {affected} |")
        lines.append("")

    total = sum(len(s.get("threats", [])) for s in data.get("subsystems", []))
    total += len(cross_cutting)
    metadata = data.get("metadata", {})
    lines.append("## Summary")
    lines.append("")
    lines.append(f"- **Total threats identified**: {total}")
    lines.append(f"- **Subsystems analyzed**: {len(data.get('subsystems', []))}")
    lines.append(f"- **Cross-cutting threats**: {len(cross_cutting)}")
    if metadata:
        lines.append(f"- **LLM calls**: {metadata.get('llm_calls', 'N/A')}")
        lines.append(f"- **Tool calls**: {metadata.get('tool_calls', 'N/A')}")
    lines.append("")

    return "\n".join(lines)


