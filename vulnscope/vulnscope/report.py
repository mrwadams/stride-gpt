"""Report generation — prioritised JSON, markdown, and console summary."""

from __future__ import annotations

from datetime import datetime, timezone
from typing import Any

from vulnscope.parsers.threat_model import ThreatModel
from vulnscope.scorer import ScoredFinding

# Score bands (composite 0-10).
BANDS = (
    ("CRITICAL", 8.0, 10.0),
    ("HIGH", 6.0, 7.9),
    ("MEDIUM", 4.0, 5.9),
    ("LOW", 0.0, 3.9),
)


def score_band(score: float) -> str:
    """Map a composite score to its band label."""
    if score >= 8.0:
        return "CRITICAL"
    if score >= 6.0:
        return "HIGH"
    if score >= 4.0:
        return "MEDIUM"
    return "LOW"


def build_report(
    scored: list[ScoredFinding],
    threat_model: ThreatModel,
    summary: str,
    *,
    generated_at: str | None = None,
    threat_model_date: str | None = None,
) -> dict[str, Any]:
    """Assemble the machine-readable prioritised report dict."""
    generated_at = generated_at or datetime.now(timezone.utc).isoformat()
    threat_model_date = threat_model_date or threat_model.raw.get("generated_at", "")

    gaps = [
        {
            "finding_id": s.finding.id,
            "classification": s.classification,
            "note": _gap_note(s),
        }
        for s in scored
        if s.classification in ("NOVEL", "OUT_OF_SCOPE")
    ]

    return {
        "metadata": {
            "application": threat_model.application_name,
            "threat_model_date": threat_model_date,
            "threat_model_format": threat_model.source_format,
            "findings_count": len(scored),
            "generated_at": generated_at,
            "tool": "VulnScope",
        },
        "executive_summary": summary,
        "prioritised_findings": [s.to_dict() for s in scored],
        "threat_model_gaps": gaps,
    }


def _gap_note(scored: ScoredFinding) -> str:
    component = scored.finding.component or "an unspecified component"
    if scored.classification == "OUT_OF_SCOPE":
        return (
            f"Affects {component}, which is absent from the threat model. "
            f"Recommend adding it to the next threat model revision."
        )
    return (
        f"Affects {component}, which is in the threat model, but covers a "
        f"STRIDE category/threat the model does not yet address. Recommend "
        f"extending the model's coverage for this component."
    )


