def render_markdown(report: dict[str, Any]) -> str:
    """Render the human-readable markdown report."""
    meta = report["metadata"]
    findings = report["prioritised_findings"]
    gaps = report["threat_model_gaps"]

    lines: list[str] = []
    lines.append(f"# VulnScope Report: {meta['application']}")
    lines.append("")
    lines.append(
        f"> Generated by VulnScope on {meta['generated_at']} — "
        f"{meta['findings_count']} finding(s) prioritised against the threat model."
    )
    lines.append("")

    # Executive summary
    lines.append("## Executive Summary")
    lines.append("")
    lines.append(report.get("executive_summary", "").strip() or "_No summary available._")
    lines.append("")

    # Counts by classification and band
    lines.append("## Overview")
    lines.append("")
    lines.extend(_counts_section(findings))
    lines.append("")

    # Top 10
    lines.append("## Top Findings")
    lines.append("")
    lines.append(
        "| Rank | Finding | Score | Band | Classification | Reasoning |"
    )
    lines.append("|---|---|---|---|---|---|")
    for rank, f in enumerate(findings[:10], 1):
        lines.append(
            f"| {rank} | {_md_cell(f['title'])} ({_md_cell(f['finding_id'])}) "
            f"| {f['composite_score']} | {score_band(f['composite_score'])} "
            f"| {f['classification']} | {_md_cell(_one_line(f['reasoning']))} |"
        )
    lines.append("")

    # Full prioritised list
    lines.append("## Full Prioritised List")
    lines.append("")
    for rank, f in enumerate(findings, 1):
        s = f["scores"]
        lines.append(
            f"### {rank}. {f['title']} "
            f"(`{f['finding_id']}`) — {f['composite_score']} "
            f"[{score_band(f['composite_score'])}, {f['classification']}]"
        )
        lines.append("")
        lines.append(
            f"- **Scores**: asset criticality {s['asset_criticality']}, "
            f"threat alignment {s['threat_alignment']}, "
            f"trust boundary exposure {s['trust_boundary_exposure']}, "
            f"STRIDE category weight {s['stride_category_weight']}"
        )
        lines.append(f"- **Reasoning**: {f['reasoning']}")
        lines.append("")

    # Threat model gaps
    lines.append("## Threat Model Gaps")
    lines.append("")
    if gaps:
        for g in gaps:
            lines.append(f"- **{g['finding_id']}** ({g['classification']}): {g['note']}")
    else:
        lines.append("_No gaps detected — every finding mapped onto a modelled component._")
    lines.append("")

    # Recommended threat model updates
    lines.append("## Recommended Threat Model Updates")
    lines.append("")
    recs = _recommendations(findings, gaps)
    if recs:
        for rec in recs:
            lines.append(f"- {rec}")
    else:
        lines.append("_No updates recommended at this time._")
    lines.append("")

    return "\n".join(lines)


def _counts_section(findings: list[dict[str, Any]]) -> list[str]:
    band_counts = {name: 0 for name, _, _ in BANDS}
    class_counts = {"CORROBORATED": 0, "NOVEL": 0, "OUT_OF_SCOPE": 0}
    for f in findings:
        band_counts[score_band(f["composite_score"])] += 1
        class_counts[f["classification"]] = class_counts.get(f["classification"], 0) + 1

    lines = ["**By score band:**", ""]
    for name, low, high in BANDS:
        lines.append(f"- {name} ({low:.1f}–{high:.1f}): {band_counts[name]}")
    lines.append("")
    lines.append("**By classification:**")
    lines.append("")
    lines.append(f"- Corroborated: {class_counts['CORROBORATED']}")
    lines.append(f"- Novel: {class_counts['NOVEL']}")
    lines.append(f"- Out of scope: {class_counts['OUT_OF_SCOPE']}")
    return lines


def _recommendations(
    findings: list[dict[str, Any]], gaps: list[dict[str, Any]]
) -> list[str]:
    recs: list[str] = []
    out_of_scope = [g for g in gaps if g["classification"] == "OUT_OF_SCOPE"]
    novel = [g for g in gaps if g["classification"] == "NOVEL"]
    if out_of_scope:
        recs.append(
            f"Add {len(out_of_scope)} component(s) referenced by out-of-scope findings "
            f"to the threat model."
        )
    if novel:
        recs.append(
            f"Extend STRIDE coverage for {len(novel)} modelled component(s) with novel "
            f"findings."
        )
    return recs


