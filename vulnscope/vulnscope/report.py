def render_console_summary(
    report: dict[str, Any], report_path: str | None = None
) -> str:
    """Render the fast-feedback console summary."""
    meta = report["metadata"]
    findings = report["prioritised_findings"]

    band_counts = {name: 0 for name, _, _ in BANDS}
    class_counts = {"CORROBORATED": 0, "NOVEL": 0, "OUT_OF_SCOPE": 0}
    for f in findings:
        band_counts[score_band(f["composite_score"])] += 1
        class_counts[f["classification"]] = class_counts.get(f["classification"], 0) + 1

    lines: list[str] = []
    lines.append(
        f"VulnScope — {meta['findings_count']} findings processed against "
        f"ThreatModel: {meta['application']}"
    )
    lines.append("")
    lines.append(f"  CRITICAL  (8.0–10.0): {band_counts['CRITICAL']} findings")
    lines.append(f"  HIGH      (6.0–7.9):  {band_counts['HIGH']} findings")
    lines.append(f"  MEDIUM    (4.0–5.9):  {band_counts['MEDIUM']} findings")
    lines.append(f"  LOW       (0–3.9):    {band_counts['LOW']} findings")
    lines.append("")
    lines.append(
        f"  Corroborated: {class_counts['CORROBORATED']}  |  "
        f"Novel: {class_counts['NOVEL']}  |  "
        f"Out of scope: {class_counts['OUT_OF_SCOPE']}"
    )
    lines.append("")
    if findings:
        top = findings[0]
        lines.append(
            f"  Top finding: {top['title']} "
            f"[score: {top['composite_score']}, {top['classification']}]"
        )
    if report_path:
        lines.append(f"  Report written to: {report_path}")
    return "\n".join(lines)


# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------


def _md_cell(text: str) -> str:
    """Escape a value for safe inclusion in a markdown table cell."""
    return str(text).replace("|", "\\|").replace("\n", " ").replace("\r", " ").strip()


def _one_line(text: str, limit: int = 160) -> str:
    flat = " ".join(str(text).split())
    return flat if len(flat) <= limit else flat[: limit - 1] + "…"
